# Rock defeats Scissors, Scissors defeats Paper, and Paper defeats Rock.
# if both choose same shape its a draw
#
# First Case
KEY = { "A": 'Rock', "B": 'Paper', "C": 'Scissor', "X": 'Rock', "Y": 'Paper', "Z": 'Scissor' }
# Second CASE
# KEY= { "A": 'Rock', "B": 'Paper', "C": 'Scissor', "X": 'Rock', "Y": 'Rock', "Z": 'Rock' }
# X means you need to lose, Y means you need to end the round in a draw, and Z means you need to win. Good luck!"

def total_score(input)
  opponent_total = []
  my_total = []
  input.each do |data|
    new_data = data.split(' ')
    opponent = new_data.first.to_sym

    me = new_data.last.to_sym
    barren(opponent, me, opponent_total, my_total)
    rock_wins(opponent, me, opponent_total, my_total)
    paper_wins(opponent, me, opponent_total, my_total)
    scissor_wins(opponent, me, opponent_total, my_total)
  end
  [opponent_total.reduce(&:+), my_total.reduce(&:+)]
end

def parse_input(input)
  input.split("\n")
end

def barren(them, us, opponent_total, my_total)
  # If both players choose the same shape, the round instead ends in a draw.
  if (KEY[them] == 'Scissor') && (KEY[us] == 'Scissor')
    opponent_total << 6 # [6, 6]
    my_total << 6
  elsif (KEY[them] == 'Paper') && (KEY[us] == 'Paper')
    # [5, 5]
    opponent_total << 5
    my_total << 5
  elsif (KEY[them] == 'Rock') && (KEY[us] == 'Rock')
    # [4, 4]
    opponent_total << 4
    my_total << 4
  end
end

def rock_wins(them, us, opponent_total, my_total)
  # Rock defeats Scissors,
  if (KEY[them] == 'Rock') && (KEY[us] == 'Scissor')
    # [7,3]
    opponent_total << 7
    my_total << 3
  elsif (KEY[us] == 'Rock') && (KEY[them] == 'Scissor')
    # [3,7]
    opponent_total << 3
    my_total << 7
  end
end

def paper_wins(them, us, opponent_total, my_total)
  # and Paper defeats Rock.
  if (KEY[them] == 'Paper') && (KEY[us] == 'Rock')
    # [8,1]
    opponent_total << 8
    my_total << 1
  elsif (KEY[us] == 'Paper') && (KEY[them] == 'Rock')
    # [1,8]
    opponent_total << 1
    my_total << 8
  end
end

def scissor_wins(them, us, opponent_total, my_total)
  # Scissors defeats Paper,
  if (KEY[them] == 'Scissor') && (KEY[us] == 'Paper')
    # [9,2]
    opponent_total << 9
    my_total << 2
  elsif (KEY[us] == 'Scissor') && (KEY[them] == 'Paper')
    # [2,9]
    opponent_total << 2
    my_total << 9
  end
end
